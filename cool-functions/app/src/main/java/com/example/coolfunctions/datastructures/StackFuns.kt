package com.example.coolfunctions.datastructures

import java.util.*

class StackFuns {

    fun fun1() {
        val stack = Stack<String>()

        // 'peek' Returns the object at the top of the Stack without removing it.
        stack.peek()
        // 'pop' Removes and returns the object at the top of the Stack.
        stack.pop()
    }
}